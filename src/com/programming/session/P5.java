package com.programming.session;

public class P5 //triangle patterns 1
{
	
	public static void main(String[] args) 
	{
		int n=20;
		
		for (int r = 1; r<=n; r++)
		{
			for (int c = 1; r>=c; c++)
			{
					System.out.print(" * ");	
			}
			
			System.out.println();
		}
		
			
		
	}
		

}
